fun main() {
    var i: Int = 22
    println("Example of int: $i")
    println("Min: " + Int.MIN_VALUE)
    println("Max: " + Int.MAX_VALUE)

    var b: Byte = 100
    println("Example of byte: $b")
    println("Min: " + Byte.MIN_VALUE)
    println("Max: " + Byte.MAX_VALUE)

    var s: Short = 20000
    println("Example of short: $s")
    println("Min: " + Short.MIN_VALUE)
    println("Max: " + Short.MAX_VALUE)

    var l: Long = 1223372036854775808
    println("Example of long: $l")
    println("Min: " + Long.MIN_VALUE)
    println("Max: " + Long.MAX_VALUE)

    var f: Float = 3.56F
    println("Example of float: $f")
    println("Min: " + Float.MIN_VALUE)
    println("Max: " + Float.MAX_VALUE)

    var d: Double = 3.56
    println("Example of double: $d")
    println("Min: " + Double.MIN_VALUE)
    println("Max: " + Double.MAX_VALUE)

    var c: Char = 'a'
    println("Example of char: $c")

    var boo: Boolean = true
    println("Example of boolean: $boo")
}